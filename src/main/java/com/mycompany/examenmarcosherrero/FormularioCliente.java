    public void leerFormulario() {
        try(BufferedReader bf = new BufferedReader(new FileReader(new File("movimientos.txt")))){
            String linea;
            while((linea = bf.readLine()) != null) {
                String[] lineamovimiento;
                lineamovimiento = linea.split("&&");
                Movimiento mov = new Movimiento(lineamovimiento[0], Double.parseDouble(lineamovimiento[1]));
                this.listaMovimientos.add(mov);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormularioCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FormularioCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void comprobarClientes() {
        try(PrintWriter pw = new PrintWriter(new FileWriter(new File("ClientesMax.txt")))) {
            for(Cliente c: this.listaClientes) {
                if(c.getTotalventas() >= 3000) {
                pw.println(c.toString());
            }
        }
        } catch (IOException ex) {
            Logger.getLogger(FormularioCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void comprobarFicheros() {
        try(PrintWriter pw = new PrintWriter(new FileWriter(new File("Errores.txt")))) {
            for(Movimiento m : this.listaMovimientos) {
                int caca = 0;
            for(Cliente c : this.listaClientes) {
                if(m.getCodigo().equals(c.getCodigo())) {
                  caca++;
                }
            }
            if (caca == 0) {
                pw.println(m.toString());
            }
            } 
        } catch (IOException ex) {
            Logger.getLogger(FormularioCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }   
     
    
    
    
    
