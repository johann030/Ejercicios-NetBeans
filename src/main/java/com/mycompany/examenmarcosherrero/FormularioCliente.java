    private void botonactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonactualizarActionPerformed
        // TODO add your handling code here:
        Cliente a = new Cliente("111111", "12345678A", "Gomez", "Juan", 1250.00);
        this.listaClientes.add(a);
        Cliente b = new Cliente("111112", "87654321B", "Perez", "Laura", 0);
        this.listaClientes.add(b);
        Cliente c = new Cliente("111113", "14253674C", "Martin", "Pepe", 20.00);
        this.listaClientes.add(c);
        Cliente d = new Cliente("111114", "98752631A", "Comes", "Cristina", 0);
        this.listaClientes.add(d);
        leerFormulario();
        for (Cliente cliente : this.listaClientes) {
            for (Movimiento movimiento : this.listaMovimientos) {
                if(cliente.getCodigo().equals(movimiento.getCodigo())) {
                    cliente.setTotalventas(cliente.getTotalventas()+movimiento.getImporte());
                    JOptionPane.showConfirmDialog(this, "Total Ventas actualizado a: " + cliente.getTotalventas() + " a el CLIENTE con codigo: " + cliente.getCodigo(), "Actualizacion", JOptionPane.DEFAULT_OPTION);
                }
            }
        }
        comprobarClientes();
        comprobarFicheros();
    }//GEN-LAST:event_botonactualizarActionPerformed

